class TestRenderFunctionDefined:
    """architecture.py exports a single render() callable with no required parameters."""

    def test_render_function_defined(self) -> None:
        """architecture.py defines a top-level render() function (Convention 6)."""
        arch_path = Path(__file__).parents[1] / "pages" / "architecture.py"
        if not arch_path.exists():
            pytest.skip("architecture.py not yet on disk — concurrent mode, expected")

        source = arch_path.read_text(encoding="utf-8")
        tree = ast.parse(source)

        top_level_functions = {
            node.name
            for node in ast.iter_child_nodes(tree)
            if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef))
        }

        assert "render" in top_level_functions, (
            f"architecture.py must define a top-level render() function. "
            f"Found: {sorted(top_level_functions)}"
        )

        # Verify render() requires no arguments via deferred import
        try:
            from streamlit_app.pages import architecture  # noqa: PLC0415

            assert callable(architecture.render), "architecture.render must be callable"
            sig = inspect.signature(architecture.render)
            required = [
                name
                for name, param in sig.parameters.items()
                if param.default is inspect.Parameter.empty
            ]
            assert not required, (
                f"render() must require no arguments; found required params: {required}"
            )
        except ImportError:
            # Concurrent mode — AST check above is sufficient
            pass
