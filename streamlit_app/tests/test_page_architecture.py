class TestModulePurityNoImportTimeStreamlitCalls:
    """architecture.py must not call any Streamlit rendering function at import time."""

    def test_module_purity_no_import_time_streamlit_calls(self) -> None:
        """architecture.py has no top-level st.* calls (Convention 6 — import purity).

        Uses AST inspection so the test works regardless of concurrent-mode state.
        """
        arch_path = Path(__file__).parents[1] / "pages" / "architecture.py"
        if not arch_path.exists():
            pytest.skip("architecture.py not yet on disk — concurrent mode, expected")

        source = arch_path.read_text(encoding="utf-8")
        tree = ast.parse(source)

        _RENDERING_CALLS = {
            "st.write",
            "st.title",
            "st.markdown",
            "st.header",
            "st.subheader",
            "st.info",
            "st.error",
            "st.warning",
            "st.metric",
            "st.dataframe",
            "st.image",
            "st.columns",
            "st.expander",
        }

        violations: list[str] = []
        for node in ast.iter_child_nodes(tree):
            # Skip function/class definitions — rendering inside render() is correct
            if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef)):
                continue
            for sub in ast.walk(node):
                if isinstance(sub, ast.Call):
                    func = sub.func
                    if (
                        isinstance(func, ast.Attribute)
                        and isinstance(func.value, ast.Name)
                        and func.value.id == "st"
                        and f"st.{func.attr}" in _RENDERING_CALLS
                    ):
                        violations.append(f"st.{func.attr}()")

        assert not violations, (
            "architecture.py has top-level Streamlit rendering calls (violates Convention 6). "
            f"Found: {violations}"
        )


