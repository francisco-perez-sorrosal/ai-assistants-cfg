class TestEmptyStateWhenNoArchitectureArtifacts:
    """Architecture page shows empty state when no architecture artifacts exist."""

    def test_empty_state_when_no_architecture_artifacts(
        self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        """No ARCHITECTURE.md, no SVGs, no developer doc → graceful empty state, no crash."""
        # Arrange: bare project root — .ai-state/ exists but no ARCHITECTURE.md or diagrams
        _seed(tmp_path, {".ai-state": {"decisions": {"drafts": {}}}})

        at = _run_architecture_page(tmp_path, monkeypatch)

        if at.exception:
            exc_msg = str(at.exception[0].message) if at.exception else ""
            if "PRAXION_PROJECT_ROOT" in exc_msg:
                pytest.fail(f"Config env not injected correctly: {exc_msg}")
        assert not at.exception, (
            f"Architecture page raised exception when no architecture artifacts exist: {at.exception}"
        )


