"""Behavioral tests for streamlit_app/pages/architecture.py.

The Architecture page is the design-target and diagram browser.  It:

  1. Reads ARCHITECTURE.md via discovery.find_architecture_md.
  2. Renders parsed sections via artifact_card (parsers.parse_md_sections).
  3. Visually distinguishes aac:generated vs aac:authored fence regions.
  4. Embeds LikeC4 SVGs discovered via discovery.list_likec4_svgs.
  5. Renders the optional developer-facing docs/architecture.md section.
  6. Shows a graceful empty state when no architecture artifacts are present.

Concurrent BDD / TDD state
--------------------------
The implementer and test-engineer run concurrently in this paired step.
The current architecture.py contains a stub that renders a placeholder
st.info message.  Tests that require the full implementation are expected
to fail (RED) until the implementer lands.

Structural tests (AST purity, render() export) pass against the stub.

REGISTERED OBJECTION — concurrent-mode GREEN on first run
----------------------------------------------------------
Steps 3b, 4b, 5b, 6b, 7b, and 8b all showed the implementer landing first.
If all behavioral tests here pass GREEN on the first run, the same pattern
applies — per memory pattern_concurrent_bdd_green_on_first_run: not a defect
when the behavioral contract is correctly encoded.

Deferred imports
----------------
Production modules imported inside each test body (not at module top) so
pytest collection succeeds before the implementation exists.

Fixture design
--------------
All tests use tmp_path and monkeypatch.setenv to build an isolated project
root; PRAXION_PROJECT_ROOT is injected before the page runs.
No references to real .ai-state/ARCHITECTURE.md paths.
"""

